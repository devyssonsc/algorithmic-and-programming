#Exercício 4: Dada uma temperatura expressa em graus Celsius, converter e escrever na sua equivalente em Fahrenheit.
#Fórmula de conversão: 𝐹 =9/5× º𝑐 + 32
#Exemplo com DATA 5
#Qual a temperatura em Celsius 5
#5º Celsius equivale a 41º Fahrenheit.
