# Exercício 1: Pretende-se saber o preço a pagar por um determinado número de canetas,
# sabendo que uma caneta custa 0.5 euros.
# Exemplo com DATA 4
# Quantas canetas? 4
# Vai pagar 2 euros por 4 canetas.


# 1. Ler o número de canetas
print('Quantas canetas?')
Canetas=int(input())

#2. Calcular o preço a pagar
Preco=Canetas*0.5

# 3 Escrever preço a pagar
print ('Vai pagar ', Preco, ' euros por ', Canetas, 'canetas')

