#Exercício 3: Calcular a média de idades de dois irmãos.
#Exemplo com DATA 10, 12
#Qual a idade do primeiro irmão? 10
#Qual a idade do segundo irmão? 12
#A média de idades dos dois irmãos é 11 anos.
print('Qual a idade do primeiro irmao?')
Idade1 = int(input())
print('Qual a idade do segundo irmao?')
Idade2 = int(input())
media = float((Idade1+Idade2)/2)
print('A média de idades dos dois irmãos é ', media, 'anos.')
